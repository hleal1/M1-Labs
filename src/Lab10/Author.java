package Lab10;

public class Author {
	
	protected String firstName;
	protected String middleNameInital;
	protected String lastName;
	protected String country;

	public Author (String first, String middle, String last, String country)
	{
		this.firstName = first;
		this.middleNameInital = middle;
		this.lastName = last;
		this.country = country;					
	}
}

